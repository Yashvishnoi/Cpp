#include<iostream>
#include<cmath>
using namespace std;

int main() {
	cout << pow(2,3) <<"\n";
	// returns "2" to the power "3"
	
	cout<<sqrt(4) <<"\n";
	// return square root of "4"
	
	cout<<log(2) <<"\n";
	// return log of "2"
	
	cout<<round(1.5) <<"\n";
	// return round number of "1.5"
	 
	cout<<abs(-4) <<"\n";
	// return absolute value of "-4"
	
	cout<<ceil(1.5) <<"\n";
	// return nearest integer of "1.5"
	
	cout<<fabs(-1.5) <<"\n";
	// returns  absolute value  of float "-1.5"
	
 	cout<<fmin(1,2) <<"\n";
	// returns  lowest value
	
	
	cout<<fmax(1,2) <<"\n";
	// returns highest value
	
	 
	return 0;
}
